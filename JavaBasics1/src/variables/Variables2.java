package variables;
class Variables2 
{
	public static void main(String[] args) 
	{
		System.out.println("Program Starts....");
		System.out.println("We are learning Java Variables.\n************* Local Variable *********");
		/*
		//declare a variable: datatype variableName;		
		//initiliaze a variable: variableName = value;

		// datatype variableName=value;
		*/
		int age = 25;
		long empId=1245;
		float roi=10.45f;
		double salary=95000.56;
		boolean statusCheck=true;
		char grade='A';
		System.out.println("int age: "+age);
		System.out.println("long empId: "+empId);
		System.out.println("float roi: "+roi);
		System.out.println("double salary: "+salary);
		System.out.println("boolean statusCheck: "+statusCheck);
		System.out.println("char grade: "+grade);		
		System.out.println("Program Ends....");
	}
}